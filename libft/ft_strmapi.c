#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char			*str;
	unsigned int	taille;
	unsigned int	i;

	if (s == NULL)
		return (0);
	taille = ft_strlen(s);
	str = malloc(taille + 1 * sizeof(char));
	if (str == NULL)
		return (0);
	i = 0;
	while (s[i])
	{
		str[i] = (*f)(i, s[i]);
		i++;
	}
	str[i] = '\0';
	return (str);
}
